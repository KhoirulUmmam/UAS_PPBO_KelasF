    private void munculkan() {
        int row = model.getRowCount();
        for(int i = 0; i < row; i++){
            model.removeRow(0);
        }
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/dbkedaikopi", "root", "");
            ResultSet resset = conn.createStatement().executeQuery("SELECT * FROM tb_kedaikopi");
            while(resset.next()){
                String [] data = {resset.getString(1), resset.getString(2), resset.getString(3), resset.getString(4)};
                model.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Kedai.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
