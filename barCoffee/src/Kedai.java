    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        int a = tabel.getSelectedRow();
        
        if(a > -1){
            id.setText(model.getValueAt(a, 0).toString());
            nama_kopi.setText(model.getValueAt(a, 1).toString());
            harga.setText(model.getValueAt(a, 2).toString());
            kategori.setText(model.getValueAt(a, 3).toString());
        }
    }//GEN-LAST:event_tabelMouseClicked

